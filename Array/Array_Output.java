public class Array_Output {
    public static void main(String[] args){
        String[] greeting = new String[3];
        greeting[0] = "I";
        greeting[1] = "am";
        greeting[2] = "Runze";
        for (int i = 0; i < greeting.length; i++){
            System.out.println(greeting[i]);
        }
    }
}
