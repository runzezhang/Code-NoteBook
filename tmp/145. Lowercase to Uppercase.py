# Description
# Convert a lowercase character to uppercase.
# Example
# Example 1:

# Input: 'a'
# Output: 'A'
# Example 2:

# Input: 'b'
# Output: 'B'

class Solution:
    """
    @param character: a character
    @return: a character
    """
    def lowercaseToUppercase(self, character):
        # write your code here
        return character.upper()